import { useQuery } from '@tanstack/react-query'
import { formatCurrency } from '@/shared/utils/cn'
import api from '@/shared/services/api'

interface DashboardData {
  totalAnimals: number
  todayConsultations: number
  activeHospitalizations: number
  petshopAttendancesInProgress: number
  upcomingVaccines: number
  todaySalesTotal: number
  financialBalance: number
  waitingPatients: number
}

export function DashboardPage() {
  const { data, isLoading } = useQuery({
    queryKey: ['dashboard'],
    queryFn: async () => {
      const response = await api.get('/bff/v1/dashboard')
      return response.data.data as DashboardData
    },
  })

  if (isLoading) {
    return (
      <div className="flex items-center justify-center h-64">
        <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-primary-600"></div>
      </div>
    )
  }

  return (
    <div className="space-y-6">
      <div className="flex items-center justify-between">
        <h1 className="text-2xl font-bold text-gray-900">Dashboard</h1>
        <p className="text-sm text-gray-500">
          Visão geral do sistema
        </p>
      </div>

      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-6">
        <StatCard
          title="Total de Animais"
          value={data?.totalAnimals ?? 0}
          icon="paw"
          color="blue"
        />
        <StatCard
          title="Consultas Hoje"
          value={data?.todayConsultations ?? 0}
          icon="calendar"
          color="green"
        />
        <StatCard
          title="Internações Ativas"
          value={data?.activeHospitalizations ?? 0}
          icon="bed"
          color="purple"
        />
        <StatCard
          title="Petshop em Andamento"
          value={data?.petshopAttendancesInProgress ?? 0}
          icon="store"
          color="orange"
        />
      </div>

      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-6">
        <StatCard
          title="Vacinas Próximas"
          value={data?.upcomingVaccines ?? 0}
          icon="syringe"
          color="red"
        />
        <StatCard
          title="Vendas do Dia"
          value={formatCurrency(data?.todaySalesTotal ?? 0)}
          icon="currency"
          color="emerald"
        />
        <StatCard
          title="Saldo Financeiro"
          value={formatCurrency(data?.financialBalance ?? 0)}
          icon="chart"
          color="teal"
        />
        <StatCard
          title="Pacientes Aguardando"
          value={data?.waitingPatients ?? 0}
          icon="list"
          color="yellow"
        />
      </div>
    </div>
  )
}

